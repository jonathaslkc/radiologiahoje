                        <h4 class="center" id="cod_etica_txt">CONSELHO NACIONAL DE TÉCNICOS EM RADIOLOGIA<br>CÓDIGO DE ÉTICA  DOS PROFISSIONAIS DAS TÉCNICAS  RADIOLÓGICAS<br>PREÂMBULO</h4>
                        <p>
                            I - O código de Ética Profissional enuncia os fundamentos éticos e as condutas necessárias a boa e
                            honesta praticas das profissões do Tecnólogo, Técnico e Auxiliar de Radiologia e relaciona direitos
                            e deveres correlatos de seus profissionais inscritos  no sistema CONTER/CRTRs e das pessoas
                            jurídicas correlatas.<br>
                            II - Para o exercício da profissão de Tecnólogo, Técnico ou Auxiliar de Radiologia impõe-se a
                            inscrição no Conselho Regional da respectiva Jurisdição.<br>
                            III - Os preceitos deste Código de Ética têm alcance sobre os profissionais das Técnicas
                            Radiológica e Auxiliares de Radiologia, quaisquer que sejam seus níveis de formação, modalidades
                            e especializações.
                        </p>
                        <h4 class="center">CAPITULO I<br>DA PROFISSÃO</h4>
                        <p>
                            Art. 1º - É objeto da profissão do Tecnólogo, Técnico e Auxiliar em Radiologia o disposto na Lei nº
                            7.394, de 29 de outubro de 1985, regulamentada pelo Decreto nº 92.790 de 17 de junho de 1986,
                            nas seguintes áreas;<br>
                            I – Radiologia, no setor de diagnostico médico;<br>
                            II – Radioterápicas, no setor de Terapia medica;<br>
                            III – Radioisotopicas, no setor de Radioisótopos;<br>
                            IV – Radiologia Industrial, no setor Industrial;<br>
                            V – De medicina nuclear.
                        </p>
                        <h4 class="center">CAPITULO II<br>NORMAS FUNDAMENTAIS</h4>
                        <p>
                            Art. 2º - O Tecnólogo, Técnico e Auxiliar de Radiologia, no desempenho de suas atividades
                            profissionais, deve respeitar integralmente a dignidade da pessoa Humana destinatária de seus
                            serviços, sem restrição de raça nacionalidade, partido político, classe social e religião.<br>
                            Parágrafo Primeiro – Respeitar integralmente a dignidade da pessoa humana destinatária de seus
                            serviços, sem restrição de raça, nacionalidade, sexo, idade, partido político, classe social e religião.<br>
                            Parágrafo segundo – Pautar sua vida observando na profissão e fora dela, os mais rígidos princípios
                            morais para a elevação de sua dignidade pessoal, de sua profissão e de toda a classe, exercendo sua
                            atividade com zelo, probidade e decoro, em obediência aos preceitos da ética profissional, da moral,
                            do civismo e da legislação em vigor.<br>
                            Parágrafo terceiro – Dedicar-se ao aperfeiçoamento e atualização de seus conhecimentos técnicos
                            científicos e a sua cultura geral, e assim para a promoção do bem estar social. <br>
                            Art. 3º - O Tecnólogo, Técnico e Auxiliar de Radiologia, no exercício de sua função profissional,
                            complementará a definição de suas responsabilidades, direitos e deveres nas disposições da
                            legislação especial ou em geral, em vigor no país.  
                        </p>
                        <h4 class="center">CAPITULO III<br>DAS RELAÇÕES COM O CLIENTE/PACIENTE</h4>
                        <p>
                            Art. 4º - O alvo de toda a atenção do Tecnólogo, Técnico e Auxiliar em Radiologia é o
                            cliente/paciente, em beneficio do qual deverá agir com o máximo de zelo e o melhor de sua
                            capacidade técnica e profissional.<br>
                            Art. 5º - Fica vedado ao Tecnólogo, Técnico e Auxiliar em Radiologia, obter vantagem indevida
                            aproveitando-se da função ou em decorrência dela, sejam de caráter físico, emocional econômica ou
                            política, respeitando a integridade física e emocional do cliente/paciente, seu pudor natural, sua
                            privacidade e intimidade.<br>
                            Art. 6º – Ao Tecnólogo, Técnico e Auxiliar em Radiologia é expressamente vedado fornecer ao
                            cliente/paciente, informações diagnósticas verbais ou escritas sobre procedimentos realizados.         
                        </p>
                        <h4 class="center">CAPITULO IV<br>DAS RELAÇÕES COM OS COLEGAS</h4>
                        <p>
                            Parágrafo primeiro – Participar de qualquer ato de concorrência desleal contra colegas, valendo-se
                            de vantagem, física, emocional, política ou religiosa.<br>
                            Parágrafo segundo – Assumir emprego, cargo ou função de um profissional demitido ou afastado
                            em represália a atitude de defesa de movimentos legítimos da categoria e da aplicação deste código.<br>
                            Parágrafo terceiro – Posicionar-se contrariamente a movimentos da categoria, com a finalidade de
                            obter vantagens.<br>
                            Parágrafo quarto – Ser conivente em erros técnicos, infrações éticas e com o exercício irregular ou
                            ilegal da profissão.<br>
                            Parágrafo quinto – Compactuar, de qualquer forma, com irregularidades dentro do seu local de
                            trabalho, que venham prejudicar sua dignidade profissional, devendo denunciar tais situações ao
                            Conselho Regional de sua jurisdição.<br>
                            Parágrafo sexto – Participar da formação profissional e de estágios irregulares.
                        </p>
                        <h4 class="center">CAPITULO V<br>DAS RELAÇÕES COM OUTROS PROFISSIONAIS</h4>
                        <p>
                            Art. 8º – O Tecnólogo, Técnico e Auxiliar em Radiologia tem obrigação de adotar uma atitude de
                            solidariedade e consideração a seus colegas, respeitando sempre os padrões de ética profissional e
                            pessoal estabelecidos neste código, indispensáveis a harmonia e a elevação de sua profissão, dentro
                            da classe e no conceito da sociedade.<br>
                            Parágrafo Único – As relações do Tecnólogo, Técnico e Auxiliar de Radiologia, com os demais
                            profissionais, no exercício da sua profissão, devem basear-se no respeito mutuo, na liberdade e
                            independência profissional de cada um, buscando sempre o interesse e o bem estar do
                            cliente/paciente.<br>
                            Art. 9º - O Tecnólogo, Técnico e Auxiliar em Radiologia se obriga, caso seja solicitado seu
                            depoimento em processo administrativo, judicial ou procedimento de dispensa por justa causa a
                            depor compromissado com a verdade, sobre fatos que envolvam seus colegas, de que tenha
                            conhecimento em razão do ambiente profissional, jamais dando falso testemunho para obter
                            vantagens com alguma das partes ou prejudicar injustamente os mesmos.<br>
                            Parágrafo único – Ao Tecnólogo, Técnico e Auxiliar em Radiologia é terminantemente vedada a
                            obtenção de informações prejudiciais ao seu colega, utilizando-se de meio ilícito ou imoral a fim de
                            obter qualquer vantagem pessoal e profissional, em detrimento da imagem do outro.  <br>
                            Art. 10 – O Tecnólogo, Técnico e Auxiliar em Radiologia deve reconhecer as limitações de suas
                            atividades, procurando desempenhar suas funções segundo as prescrições medica e orientações
                            técnicas do Coordenador Técnico do serviço.   <br>
                            Art. 11 – Quando investido em função de Chefe,  Coordenador ou Supervisor, deve o Tecnólogo ou
                            Técnico em Radiologia, em suas relações com colegas, auxiliares e demais funcionários, pautar sua
                            conduta pelas normas do presente Código, exigindo deles igualmente fiel observância dos preceitos
                            éticos.
                        </p>
                        <h4 class="center">CAPITULO VI<br>DAS RELAÇÕES COM OS SERVIÇOS EMPREGADORES</h4>
                        <p>
                            Art. 12 – O Tecnólogo ou Técnico em Radiologia deverá abster-se junto aos clientes de fazer critica
                            aos serviços hospitalares, assistenciais, e a outros profissionais, devendo encaminhá-la, por escrito,
                            à consideração das autoridades competentes.<br>
                            Art. 13 – Deverá o Tecnólogo ou Técnico em Radiologia, empregado ou sócio, respeitar as normas
                            da instituição utilizadora dos seus serviços, desde que estas não firam o presente Código de Ética.<br>
                             Art. 14 – O Tecnólogo ou Técnico em Radiologia, tem o dever de apontar falhas nos regulamentos
                            e normas das instituições em que trabalhe, quando as julgar indignas do exercício da profissão ou
                            prejudiciais aos clientes, devendo dirigir-se, nesses casos, aos órgãos competentes e ao Conselho
                            Regional de Técnicos em Radiologia de sua jurisdição.<br>
                            Parágrafo único – O Tecnólogo, Técnico e Auxiliar em Radiologia, uma vez constatado condições
                            indignas de trabalho que possam prejudicar a si ou a seus clientes/pacientes deve encaminhar, por
                            escrito, à Direção da instituição relatório e pedido de providencias, caso persistam comunicar às
                            autoridades competentes.<br>
                            Art. 15 – O Tecnólogo, Técnico e Auxiliar em Radiologia deve recusar-se  a executar atividades que
                            não sejam de sua competência legal. 
                        </p>
                        <h4 class="center">CAPITULO VII<br>DAS RESPONSABILIDADES PROFISSIONAIS</h4>
                        <p>
                            Art. 16 – O Tecnólogo, Técnico e Auxiliar em Radiologia deve:<br>
                            Parágrafo primeiro – Preservar, em sua conduta, a honra, a nobreza e a dignidade da profissão,
                            zelando pelo seu caráter de essencialidade e indispensabilidade, pela sua reputação pessoal e
                            profissional.<br>
                            Parágrafo segundo – Reconhecer as possibilidades e limitações no desempenho de suas funções
                            profissionais e só executar técnicas radiológicas, radioterápicas, nuclear e industrial, mediante
                            requisição ou solicitação do especialista. <br>
                            Parágrafo terceiro – Assumir civil e penalmente responsabilidades por atos profissionais danosos ao
                            cliente/paciente a que tenha dado causa por imperícia, imprudência, negligencia ou omissão.<br>
                            Parágrafo quarto - Assumir sempre a responsabilidade profissional de seus atos, deixando de
                            atribuir, injustamente, seus insucessos a terceiros ou a circunstancias ocasionais, devendo primar
                            pela boa qualidade do seu trabalho.<br>
                            Art. 17 – O Tecnólogo, Técnico e Auxiliar em Radiologia, deve observar, rigorosa e
                            permanentemente, as normas legais de proteção contra as radiações ionizantes no desempenho de
                            suas atividades profissionais, para resguardar sua saúde, a do cliente, de seus auxiliares e de seus
                            descendentes.<br>
                            Art. 18 – Será de responsabilidade do Tecnólogo ou Técnico em Radiologia, que estiver operando o
                            equipamento emissor de Radiação a isolação do local, a proteção das pessoas nas áreas irradiadas e
                            a utilização dos equipamentos de segurança, em conformidade com as normas de proteção
                            Radiológica vigentes no País.<br>
                            Art. 19 – O Tecnólogo, Técnico e Auxiliar em Radiologia é obrigado a exigir dos serviços em que
                            trabalhe todo o equipamento indispensável de proteção radiológica, cumprindo determinações
                            legais e adotando o procedimento descrito no parágrafo único do art. 16 deste Código, podendo,
                            caso persistam,  negar-se a executar exames, procedimentos ou tratamentos na falta dos mesmos.<br>
                            Art. 20 – O Tecnólogo, Técnico e Auxiliar em Radiologia jamais poderá deixar de cumprir as
                            normas emanadas dos Conselhos Federal e Regionais de Técnicos em Radiologia e de atender as
                            suas requisições administrativas, intimações ou notificações no prazo determinado.<br>
                             Art. 21 - A fim de garantir o acatamento e cabal execução deste Código, cabe ao Tecnólogo,
                            Técnico e o Auxiliar em Radiologia comunicar ao Conselho Regional de Radiologia, com discrição
                            e fundamento, fatos de que tenha conhecimento e que caracterizem possível infrigência do presente
                            Código e das normas que regulam o exercício das Técnicas Radiológicas no país.
                        </p>
                        <h4 class="center">CAPITULO VIII<br>DA REMUNERAÇÃO PROFISSIONAL</h4>
                        <p>
                            Art. 22 – Os Serviços profissionais do Tecnólogo, Técnico e Auxiliar em Radiologia, devem ser
                            remunerados em níveis compatíveis com a dignidade da profissão e sua importância reconhecida na
                            área profissional a que pertence.<br>
                            Parágrafo único – Ao candidatar-se a emprego, deve procurar estipular as suas pretensões salariais,
                            nunca aceitando ofertas inferiores às estabelecidas na legislação em vigor e nas negociações feitas
                            pelo órgão de classe.<br>
                            Art. 23 – A remuneração do Tecnólogo, Técnico e Auxiliar em Radiologia será composta de
                            salários, comissões e produtividade, por qualidade, participações em faturamento de empresas ou
                            departamentos radiológicos, cursos, aulas, palestras, supervisão, chefia e outras receitas por serviços
                            efetivamente prestados, sendo terminantemente vedado o recebimento de gratificações extras de
                            cliente/paciente ou acompanhante.
                        </p>
                        <h4 class="center">CAPÍTULO IX<br>DO SIGILO PROFISSIONAL</h4>
                        <p>
                            Art. 24 – Constitui infração ética:<br>
                            I – revelar, sem justa causa, fato sigiloso de que tenha conhecimento em razão do exercício de sua
                            profissão;<br>
                            II – negligenciar na orientação de seus colaboradores quanto ao sigilo profissional;<br>
                            III – fazer referência a casos clínicos identificáveis, exibir clientes ou seus retratos em anúncios
                            profissionais ou na divulgação de assuntos Radiológicos em programas de rádio, televisão ou
                            cinema, e em artigos entrevistas ou reportagens em jornais, revistas, congressos e/ou simpósios, ou
                            outras publicações legais, salvo se autorizado pelo cliente/paciente ou responsável, <br>
                            Parágrafo único – Compreende-se como justa causa, principalmente:<br>
                            colaboração com a justiça nos casos previstos em Lei; <br>
                            notificação compulsória de doença; <br>
                            perícia radiológica nos seus exatos limites; <br>
                            estrita defesa de interesse legítimo dos profissionais inscritos; <br>
                            revelação de fato sigiloso ao responsável pelo incapaz. 
                        </p>
                        <h4 class="center">CAPÍTULO X<br>DA PESQUISA CIENTÍFICA </h4>
                        <p>
                            Art. 25 – Constitui infração ética:<br>
                            I – desatender às normas do órgão competente à Legislação sobre pesquisa envolvendo as
                            Radiações;<br>
                            II – utilizar-se de animais de experimentação sem objetivos claros e honestos de enriquecer os
                            horizontes do conhecimento das Radiações e, conseqüentemente, de ampliar os benefícios à
                            sociedade;<br>
                            III – realizar pesquisa em ser humano sem que este ou seu responsável, ou representante legal, tenha
                            dado consentimento, livre e estabelecido, por escrito, sobre a natureza das conseqüências da
                            pesquisa;<br>
                            IV – usar, experimentalmente, sem autorização da autoridade competente, e sem o conhecimento e
                            o consentimento prévios do cliente ou de seu representante legal, qualquer tipo de terapêutica ainda
                            não liberada para uso no País;<br>
                            V – manipular dados da pesquisa em benefício próprio ou de empresas e/ou instituições;<br>
                            VI – divulgar assunto ou descoberta de conteúdo inverídico;<br>
                            VII – utilizar-se sem referência ao autor ou sem sua autorização expressa de dados ou informações
                            publicadas ou não.<br>
                            VIII – publicar em seu nome trabalho científico do qual não tenha participado ou atribui-se autoria
                            exclusiva quando houver participação de subordinado ou outros profissionais, tecnólogos/técnicos/
                            Auxiliar ou não.
                        </p>
                        <h4 class="center">CAPÍTULO XI<br>DAS ENTIDADES COM ATIVIDADES NO AMBITO DA RADIOLOGIA </h4>
                        <p>
                            Art. 26 – Aplicam-se as disposições deste Código de ética e as normas dos Conselhos de Radiologia
                            a todos aqueles que exerçam a radioimaginologia, ainda que de forma indireta, sejam pessoas físicas
                            ou jurídicas.<br>
                            Art. 27 – Os profissionais quando proprietário ou responsável Técnico responderão solidariamente
                            com o infrator pelas infrações éticas cometidas.<br>
                            Art. 28 – As entidades mencionadas no artigo 26 ficam obrigadas a:<br>
                            Parágrafo primeiro - Indicar o Supervisor técnico, de acordo com a legislação vigente;<br>
                            Parágrafo segundo - Manter a qualidade técnica científica dos trabalhos realizados;<br>
                            Parágrafo terceiro - Propiciar ao profissional, condições adequadas de instalações, recursos
                            materiais, humanos e tecnológicos os quais garantam o seu desempenho pleno e seguro.
                        </p>
                        <h4 class="center">CAPITULO XII<br>DOS CONSELHOS NACIONAL E REGIONAIS E DA OBSERVÂNCIA E APLICAÇÃO DO CÓDIGO</h4>
                        <p>
                            Art. 29 – Compete somente ao Conselho Nacional e aos Conselhos Regionais orientar, disciplinar e
                            fiscalizar o exercício da profissão de Tecnólogo, Técnico e Auxiliar em Radiologia, bem como
                            aplicação de medidas disciplinares que possam garantir a fiel observância do presente Código.<br>
                            Parágrafo único – Ao se inscrever em qualquer Conselho Regional o Tecnólogo, Técnico e Auxiliar
                            em Radiologia assume tacitamente a obrigação de respeitar o presente Código.
                        </p>
                        <h4 class="center">CAPITULO XIII<br>DAS PENALIDADES</h4>
                        <p>
                            Art. 30 – Os preceitos deste Código são de observância obrigatória e sua violação sujeitará o
                            infrator e quem, de qualquer modo, com ele concorrer para a infração, ainda de forma omissa as
                            seguintes penas:<br>
                            Advertência confidencial <br>
                            Censura Confidencial <br>
                            Censura Publica em publicação oficial; <br>
                            Multa no valor de até 10 anuidades; <br>
                            Suspensão do exercício profissional por 30 dias; <br>
                            Cassação do exercício profissional “ad referendum” do Conselho Nacional <br>
                            Parágrafo Único – Salvo nos casos de manifesta gravidade, que exijam aplicação mediata das
                            penalidades mais sérias, a imposição das penas obedecerá a graduação conforme a reincidência;<br>
                            Art. 31 – Considera-se de manifesta gravidade, principalmente:<br>
                            I - Levantar falso testemunho ou utilizar-se de má-fé e meios ilícitos contra colega de profissão com
                            o objetivo de prejudica-lo;<br>
                            II - Acobertar ou ensejar o exercício ilegal ou irregular da profissão;<br>
                            III - Manter atividade profissional durante a vigência de penalidade suspensiva;<br>
                            IV - Exercer atividade privativa de outros profissionais;<br>
                            V - Exercer, o Auxiliar, atividade inerente ao Tecnólogo e ao Técnico em Radiologia;<br>
                            VI - Ocupar cargo cujo profissional dele tenha sido afastado por motivo de movimento classista;<br>
                            VII - Ofender a integridade física ou moral do colega de profissão ou do cliente/paciente;<br>
                            VIII - Atentar contra o decoro e a moral dos dirigentes do órgão a que pertence.<br>
                            Art. 32 – São circunstâncias que podem atenuar a pena:<br>
                            I – Não ter sido antes condenado por infração ética;<br>
                            II – Ter reparado ou minorado o dano.<br>
                            Art. 33 – Avalia-se a gravidade pela extensão do dano e por suas conseqüências;<br>
                            Art. 34 – A pena de multa aplicada em casos de transgressões não prejudica a aplicação de outra
                            penalidade concomitantemente;<br>
                            Art. 35 – As referidas penas serão aplicadas pelos Conselhos Regionais e comunicadas ao Conselho
                            Nacional que dará ciência aos demais Conselhos Regionais.<br>
                            Art. 36 – Ao penalizado caberá recurso suspensivo ao Conselho Nacional até 30 (trinta), dias após a
                            notificação.<br>
                            Parágrafo único – A parte reclamante ou a acusação, também caberá recurso até 30 (trinta), dias
                            após o julgamento.<br>
                            Art. 37 – Em caso de reincidência, a pena de multa deverá ser aplicada em dobro.<br>
                            Art. 38 – Somente na secretaria do Conselho Regional poderão as partes ou seus procuradores terem
                            vistas do processo, tirar cópias mediante pagamento das custas, podendo, nesta oportunidade tomar
                            as notas que julgarem necessárias a defesa ou acusação.<br>
                            Parágrafo Único – É expressamente vedada a retirada de processos pelas partes ou seus
                            procuradores, sob qualquer pretexto, da secretaria do Conselho Regional, sendo igualmente vedada
                            lançar notas nos autos ou sublinhá-los de qualquer forma.
                        </p>
                        <h4 class="center">CAPITULO XIV<br>DAS DISPOSIÇÕES GERAIS</h4>
                        <p>
                            Art. 39 – As duvidas e os casos omissos serão resolvidos pelo Conselho Nacional, para o qual
                            podem ser encaminhadas consultas que, não assumindo caráter de denúncia, incorrerão nas mesmas
                            exigências de discrição e fundamentação.<br>
                            Art. 40 – Caberá ao Conselho Nacional e aos Conselhos Regionais, bem como a todo Tecnólogo,
                            Técnico e Auxiliar em Radiologia, promoverem a mais ampla divulgação do presente Código.<br>
                            Art. 41 – O presente Código de Ética do Tecnólogo, Técnico e Auxiliar em Radiologia, elaborado
                            pelo Conselho Nacional de Técnicos em Radiologia, atende ao disposto do artigo 16, do Decreto nº
                            92.790/96, de 17 de julho de 1986. 
                        </p>
                        
